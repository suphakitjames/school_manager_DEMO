async function main() {
  console.log("🏛️ Seeding administrative divisions...");

  for (const div of divisions) {
    await prisma.administrativeDivision.upsert({
      where: { id: div.order }, // use order as a rough ID for upsert
      update: {
        name: div.name,
        nameEn: div.nameEn,
        icon: div.icon,
        description: div.description,
        order: div.order,
      },
      create: {
        name: div.name,
        nameEn: div.nameEn,
        icon: div.icon,
        description: div.description,
        order: div.order,
      },
    });
    console.log(`  ✅ ${div.name}`);
  }

  console.log("\n🎉 Done! 4 divisions seeded.");
}

main()
